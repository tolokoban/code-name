function randomWords(): string[] {
    return shuffleWords(WORDS).slice(0, 25)
}


function shuffleWords(words: string[]) {
    const len = words.length
    for (let i = 0; i < len; i++) {
        const k = Math.floor(Math.random() * len)
        const temp = words[i]
        words[i] = words[k]
        words[k] = temp
    }
    return words
}


